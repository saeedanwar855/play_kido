class PhonicsModel {
  PhonicsModel({
    required this.letterName,
    required this.sound,
    required this.letterImage,
    required this.abbreviation,
  });

  factory PhonicsModel.fromJson(Map<String, dynamic> json) => PhonicsModel(
        letterName: json['letterName'] as String,
        sound: json['sound'] as String,
        letterImage: json['letter'] as String,
        abbreviation: (json['abbreviation'] as List<dynamic>)
            .map((x) => PhonicsAbbreviation.fromJson(x as Map<String, dynamic>))
            .toList(),
      );

  String letterName;
  String sound;
  String letterImage;
  List<PhonicsAbbreviation> abbreviation;

  Map<String, dynamic> toJson() => {
        'letterName': letterName,
        'sound': sound,
        'letterImage': letterImage,
        'abbreviation': List<dynamic>.from(abbreviation.map((x) => x.toJson())),
      };
}

class PhonicsAbbreviation {
  PhonicsAbbreviation({
    required this.name,
    required this.picture,
    required this.startTime,
    required this.endTime,
  });

  factory PhonicsAbbreviation.fromJson(Map<String, dynamic> json) => PhonicsAbbreviation(
        name: json['name'] as String,
        picture: json['picture'] as String,
        startTime: json['start_time'] as int,
        endTime: json['end_time'] as int,
      );
  final String name;
  final String picture;
  final int startTime; // in milliseconds
  final int endTime;

  Map<String, dynamic> toJson() => {
        'name': name,
        'picture': picture,
      };
}
